package datatypes;

public class DataTypeDemo {
    public static void main(String[] args) {
        int i = 0X10;
        System.out.println(i);

        // 0 - if we are adding 0 before any int . then value wil treated as octal number
        // 0b - binary
        // 0X  - hexadecimal

        // int is the default datatype in numeric values

        // byte - 1byte
        // short - 2 byte
        // int - 4 byte
        // long - 8 byte

        long creditCard = 1435_7281_7387_2176l;
        System.out.println(creditCard);

    }
}
